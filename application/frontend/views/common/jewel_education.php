              <h5>Ring Size Measurement by Ring’s Circumference</h5>
               <p> The circumference of a ring can be measured by its length around the inner circle. You can use below given finger size strip for measuring your exact ring size. Please follow the steps to get the accurate ring size:
                Take a printout of the below given figure B as per the given print instructions. 

                Carefully cut this printout from the image outline to get a size strip. Also give a slit inside the strip at the left end. 

                Wrap this size strip around the intended finger with the scale facing out and then slide the narrow end of the strip through the slit. 

                Pull the narrow end until the fit is snug. Also try to move that fit through the largest part of the finger, this may be the knuckle. 

                   The number that lines up at the slit represents the ring size for that finger.</p>
