     <h2 class=" mt-5"> Wonder of Nature</h2>

     <p>  Each diamond is unique and rare, taking billions of years for nature to form them. Each reflects the story of its arduous journey from deep inside the earth to a cherished object of adornment. Yet all diamonds share certain features that allow us to compare and evaluate them. These features are called the 4C’s.</p>

     <h2 class=" mt-5"> Treasure of Time </h2>

     <p>There is no other gemstone quite like a diamond. It is found in the most remote places on earth, and the fact that it forms at all is something of a miracle. It takes about one ton of rock to recover less than half a carat of rough, making diamond one of the rarest and most desired gemstones in the world. A diamond is a testament of endurance and strength- and not surprisingly, the ultimate symbol of love.</p>

