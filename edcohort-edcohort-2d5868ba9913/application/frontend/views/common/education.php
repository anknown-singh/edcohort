         <h2 class="">Carat Weight</h2>
         <p> The size of a diamond is measured, not by its dimensions, but by weight. One carat, the traditional unit of measure for diamonds, is equal to approximately 0.2 grams. You may also hear the weight of a diamond referred to in points. A point is equal to 1/100 of a carat; therefore, a 75-point diamond equals 0.75 carat. Diamonds of equal weight may appear slightly different in size, depending on their depth and proportions. Because they are quite rare, larger diamonds of gem quality are much more valuable.</p>

