         <h2 class="">Color</h2>
         <p> With the exception of some fancy colored diamonds, the most valuable diamonds are those with the least color. The color scale for transparent diamonds runs from D-F (colorless), G-J (near colorless), K-L (faint yellow), to Z (light yellow). Completely colorless diamonds are rare. <br> When diamonds are formed with traces of other minerals, rare and beautiful colors can result. These “fancy” colors range from blue to brilliant yellow to red, brown, pale green, pink, and violet. Because of their rarity, colored diamonds are highly desirable and may be quite valuable.</p>
