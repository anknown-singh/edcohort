         <h2 class="">Clarity</h2>
         <p>A diamond’s clarity is measured by the existence, or absence, of visible flaws. Tiny surface blemishes or internal inclusions — even those seen only under magnification with a jeweler’s loupe — can alter the brilliance of the diamond and, thus, effect its value. Clarity levels begin with Flawless (F & IF) and move down to Very Very Slight (VVS1 & 2), Very Slight (VS1 & 2), Slightly Included (SI1 & 2), and Included (I1, I2 & I3).</p>
