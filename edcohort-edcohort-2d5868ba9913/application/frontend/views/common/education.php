         <h2 class=""> Cut</h2>
         <p>  When you think of the cut, you probably think of the shape of the diamond. You are partially correct. While cut does refer to shape, it also refers to the proportions of how the diamond is actually cut.
Diamonds are cut into many different shapes, reflecting not only popular taste but the proportions and quality of the rough diamond. The most popular shapes include Round, Princess Cut, Cushion, Oval, Emerald, Heart Shape, and Marquise cuts. Many specialty shapes are also available.</p>

<p> A diamond’s overall proportions, as well as the size and placement of its many reflective surfaces or facets, also play a large part in “cut.” The consistency and balance of these can greatly affect how the stone captures light and reflects it back to the eye.</p>
